#include "sremu.h"

void printPacket(void* b, uint len, const char* str){
	ubyte* p = (ubyte*)b;
	printf("%s PACKET DEBUG:\n",str);
	for(uint i = 0; i < len; i++)
		printf("%.2X ",p[i]);
	printf("\n");
}